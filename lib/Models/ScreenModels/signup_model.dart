class SignupModel {
  String companyName;
  String companyAddress;
  String companyEmail;
  String ownerName;
  String ownerEmail;
  String phoneNumber;
  String password;

  SignupModel({
    required this.companyName,
    required this.companyAddress,
    required this.companyEmail,
    required this.ownerName,
    required this.ownerEmail,
    required this.phoneNumber,
    required this.password,
  });
}
